//connect to database and show the details
    private void btViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btViewActionPerformed
        try {

            Statement s = Database.getStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM employee WHERE emp_id='" + txtNIC.getText() + "'");
            while (rs.next()) {
                txtName.setText(rs.getString("emp_name"));
                txtNo.setText(rs.getString("id"));
                txtAddress.setText(rs.getString("emp_address"));
                txtAge.setText(rs.getString("age"));
                if (rs.getString("sex").equals("F")) {
                    txtSex.setText("Female");
                } else {
                    txtSex.setText("Male");
                }
                txtTpNo.setText(rs.getString("telephoneNo"));
                txtEmpType.setText(rs.getString("employe_type"));
            }
        } catch (Exception ex) {

            ex.printStackTrace();
        }// TODO add your handling code here:
    }//GEN-LAST:event_btViewActionPerformed

