    private void btOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOKActionPerformed
        String empt = txtEmpType.getText();
        ViewDoctor doc = new ViewDoctor();
        //if the empt textbox show the word doctor go to another interface which shows specific doctor details
        if (empt.equals("Doctor")) {
            ViewDoctor.txtNIC.setText(this.txtNo.getText());
            doc.setVisible(true);
            this.dispose();
        } else {
            LogReceip ad = new LogReceip();
            ad.setVisible(true);
            this.dispose();

        }

    }//GEN-LAST:event_btOKActionPerformed

